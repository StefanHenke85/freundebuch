// Mapping Event-Typ → Standard-Theme
export const EVENT_DEFAULT_THEME = {
  geburtstag: 'braun',
  hochzeit:   'rosa',
  taufe:      'blau',
  tcg:        'dunkel',
  party:      'orange',
  eigener:    'braun',
};

export function getTheme(eventTyp, themeId) {
  const id = themeId || EVENT_DEFAULT_THEME[eventTyp] || 'braun';
  return THEMES.find(t => t.id === id) || THEMES[0];
}

export function getThemeById(id) {
  return THEMES.find(t => t.id === id) || THEMES[0];
}
